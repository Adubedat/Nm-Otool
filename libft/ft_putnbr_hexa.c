#include "libft.h"

void	ft_putnbr_hexa(unsigned long int n)
{
	char	*b;

	b = "0123456789abcdef";
	if (n >= 16)
	{
		ft_putnbr_hexa(n / 16);
		ft_putnbr_hexa(n % 16);
	}
	if (n < 16)
		write(1, &b[n], 1);
}
